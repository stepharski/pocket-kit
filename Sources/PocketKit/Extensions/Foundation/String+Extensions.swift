import Foundation

public extension String {
    static let empty: String = ""
    static let space: String = " "
    static let comma: String = ","
    static let period: String = "."
    static let colon: String = ":"
    static let semicolon: String = ";"
    static let dash: String = "-"
    static let underscore: String = "_"
    static let newLine: String = "\n"
    static let tab: String = "\t"
    static let slash: String = "/"
    static let backslash: String = "\\"
    static let questionMark: String = "?"
    static let exclamationMark: String = "!"
    static let ampersand: String = "&"
    static let at: String = "@"
    static let hash: String = "#"
    static let dollar: String = "$"
    static let percent: String = "%"
}
